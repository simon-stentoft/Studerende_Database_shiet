public class StudFag {
    private int stdNr;
    private int fagNr;

    public StudFag(int stdNr, int fagNr) {
        this.stdNr = stdNr;
        this.fagNr = fagNr;
    }

    public int getStdNr() {
        return stdNr;
    }

    public void setStdNr(int stdNr) {
        this.stdNr = stdNr;
    }

    public int getFagNr() {
        return fagNr;
    }

    public void setFagNr(int fagNr) {
        this.fagNr = fagNr;
    }
}
