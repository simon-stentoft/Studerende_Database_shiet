public class Fag {
    private int fagNr;
    private String fagNavn;

    public Fag(int fagNr, String fagNavn) {
        this.fagNr = fagNr;
        this.fagNavn = fagNavn;
    }






    public int getFagNr() {
        return fagNr;
    }

    public void setFagNr(int fagNr) {
        this.fagNr = fagNr;
    }

    public String getFagNavn() {
        return fagNavn;
    }

    public void setFagNavn(String fagNavn) {
        this.fagNavn = fagNavn;
    }
}
